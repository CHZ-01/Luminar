# hw04 Comparison Operators
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

print("Greater than:",num1 > num2)
print("Lesser than:",num1 < num2)
print("Equals to:",num1 == num2)
print("Not Equals to:",num1 != num2)
print("Greater than or Equal to:",num1 >= num2)
print("Lesser than or Equal to::",num1 <= num2)
